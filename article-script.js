// Load article content based on stored ID
document.addEventListener("DOMContentLoaded", () => {
  const currentArticle = localStorage.getItem("currentArticle") || "article1"
  loadArticleContent(currentArticle)
})

function loadArticleContent(articleId) {
  const article = articles[articleId]
  if (article) {
    document.getElementById("articleTitle").textContent = article.title
    document.getElementById("articleContent").innerHTML = article.content

    // Render related articles
    const relatedContainer = document.querySelector(".articles-grid")
    relatedContainer.innerHTML = ""

    Object.entries(articles).forEach(([id, a]) => {
      if (id !== articleId && a) { // Check if article exists
        const html = `
    <div class="article-card" onclick="loadArticle('${id}')">
      <div class="article-thumbnail">
        <img src="${a.image || (a.content ? getFirstImage(a.content) : null) || './asset/placeholder.jpg'}" alt="${a.title || ''}" />
      </div>
      <div class="article-info">
        <h3 class="article-title">${a.title || ''}</h3>
        <p class="article-meta">
          ${a.category ? `<span class="article-category">${a.category}</span>` : ''}
          ${a.date ? `<span class="article-date">${a.date}</span>` : ''}
        </p>
      </div>
    </div>
    `
        relatedContainer.insertAdjacentHTML("beforeend", html)
      }
    })
  }
}


function loadArticle(articleId) {
  // Add fade out effect
  document.body.style.opacity = '0'
  document.body.style.transition = 'opacity 0.3s ease'

  setTimeout(() => {
    // Update content
    localStorage.setItem("currentArticle", articleId)
    loadArticleContent(articleId)
    window.scrollTo(0, 0)

    // Fade back in
    document.body.style.opacity = '1'

    // Update URL without reload
    const newUrl = `?article=${articleId}`
    window.history.pushState({ articleId }, '', newUrl)
  }, 300)
}

// Handle browser back/forward buttons
window.onpopstate = function (event) {
  if (event.state && event.state.articleId) {
    loadArticleContent(event.state.articleId)
    window.scrollTo(0, 0)
  }
}
function goBack() {
  window.history.back()
}

function getFirstImage(content) {
  if (!content) return null;
  const match = content.match(/<img.*?src=["'](.*?)["']/)
  return match ? match[1] : null
}